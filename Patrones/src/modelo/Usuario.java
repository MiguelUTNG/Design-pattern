package modelo;

/**
 *
 * @author Luis Ventura
 */
public class Usuario {
    private String nombre, contrasena, correo, apellido;
    private int id, edad;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getContrasena() {
        return contrasena;
    }

    public void setContrasena(String contrasena) {
        this.contrasena = contrasena;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public Usuario() {
    }

    public Usuario(String nombre, String contrasena, String correo, String apellido, int id, int edad) {
        this.nombre = nombre;
        this.contrasena = contrasena;
        this.correo = correo;
        this.apellido = apellido;
        this.id = id;
        this.edad = edad;
    }
    
}
